package miscellaneous;

public class IntegerCache {

	public static void main(String[] args) {

		Integer i1 = 125;
		Integer i2 = 127;
		System.out.println(i1==i2);


		 i1 = new Integer(4);
		 i2 = new Integer(4);
		System.out.println(i1==i2); 


		 i1 = 4;
		 i2 = 5;
		System.out.println(i1==i2);

	}

}
